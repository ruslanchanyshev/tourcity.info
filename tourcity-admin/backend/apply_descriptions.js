async function updateDescriptions() {
  try {
    console.log('[UpdateDesc] Fetching current sheet data...');
    const data = await googleSheetsService.getSheetData();
    if (!data || data.length < 2) return;

    const headers = data[0];
    const rows = data.slice(1);
    const nameRuIdx = headers.indexOf('name_ru');
    const descRuIdx = headers.indexOf('desc_ru');

    if (descRuIdx === -1) {
      console.error('Column desc_ru not found!');
      return;
    }

    let updatedCount = 0;

    for (let i = 0; i < rows.length; i++) {
        const row = rows[i];
        const name = row[nameRuIdx];
        
        if (DESCRIPTIONS[name]) {
            console.log(`[UpdateDesc] Updating description for ${name}...`);
            const updatedRowValues = [...row];
            updatedRowValues[descRuIdx] = DESCRIPTIONS[name];
            
            await googleSheetsService.updateRow(i, updatedRowValues);
            updatedCount++;
        }
    }

    console.log(`[UpdateDesc] Finished! Updated ${updatedCount} descriptions.`);
  } catch (err) {
    console.error('[UpdateDesc] ERROR:', err);
  }
}

updateDescriptions();
